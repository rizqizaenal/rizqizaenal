    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // ubah
        try {
            PreparedStatement ps = con.prepareStatement("UPDATE tbl_pegawai SET nama_pegawai=?, email=?,password=?,handphone=?,level=? WHERE id_pegawai=?");
            ps.setString(1, nama_pegawai.getText());
            ps.setString(2, email.getText());
            ps.setString(3, password.getText());
            ps.setString(4, no_hp.getText());
            ps.setString(5, jabatan.getSelectedItem().toString());
            ps.setString(6, id_pegawai.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"data telah di update");
            refreshTable();
            nama_pegawai.setText("");
            email.setText("");
            password.setText("");
            no_hp.setText("");
            jabatan.setSelectedItem("");
            id_pegawai.setText("");
        } catch(Exception e) {
        System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

