private void refreshTable(){
    tm = new DefaultTableModel(
    null,
    new Object[] {"ID PEGAWAI", "NAMA PEGAWAI", "EMAIL", "PASSWORD", "NO. HANDPHONE", "JABATAN"}
    );
jTable2.setModel(tm);
tm.getDataVector().removeAllElements();

try {
    PreparedStatement s = con.prepareStatement("SELECT * FROM tbl_pegawai");
    ResultSet r = s.executeQuery();
    while(r.next()) {
        Object[] data = {
            r.getString(1),
            r.getString(2),
            r.getString(3),
            r.getString(4),
            r.getString(5),
            r.getString(6)
                
        };
        tm.addRow(data);
    }
} catch(Exception e) {
    System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
}

}

